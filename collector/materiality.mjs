// ציון מהותיות מבוסס-כללים (1-10) — שלב ביניים לפני מנוע ה-LLM.
// פילוסופיה: רק אירועים שמשנים החלטת השקעה מקבלים ציון גבוה.
// מכניקת שוק שגרתית (הנפקות, תוצאות מכרז, תשקיפים) = ציון נמוך.

const RULES = [
  // ── נמוך (1-3): שגרה/טכני — נבדק ראשון כדי לתפוס לפני מילים גבוהות ──
  { re: /כתובת|תקנון|נוסח|תיקון טעות|פרטים טכניים|זימון אסיפה|מרשם בעלי|פרוטוקול/, w: 2, dir: 'neutral' },
  { re: /הצעת מדף|תשקיף|הנפקת אג|הנפקת אגרות|כתבי התחייבות|הרחבת סדרה|תוצאות מכרז|תוצאות הנפק|נע"מ|מכרז מוסדי|הקצאת אג|פדיון|ריבית|דירוג מעלות|דירוג מידרוג/, w: 3, dir: 'neutral' },

  // ── גבוה (8-10): אירועים מהותיים באמת ──
  { re: /דוח(?:ות)? כספי|דוח רבעוני|דוח שנתי|דוח תקופתי|תוצאות כספיות|דוח רווח/, w: 9, dir: 'neutral' },
  { re: /מיזוג חברות|עסקת מיזוג|הצעת רכש|השתלטות|רכישת השליטה|רכישת חברה|רכישת פעילות|מכירת פעילות|רכישה מוצעת/, w: 9, dir: 'bull' },
  { re: /אזהרת רווח|הרעה מהותית|חדלות פירעון|הקפאת הליכים|התראה על|קריסת/, w: 9, dir: 'bear' },
  { re: /זכייה|זכיה|הסכם מהותי|התקשרות מהותית|חוזה משמעותי|שיתוף פעולה אסטרטגי|אבן דרך/, w: 8, dir: 'bull' },

  // ── בינוני (5-7) ──
  { re: /מנכ"ל|יו"ר|דירקטוריון|נושא משרה בכיר|מינוי מנכ|התפטרות|סיום כהונה/, w: 7, dir: 'neutral' },
  { re: /דיבידנד|חלוקת רווחים/, w: 7, dir: 'bull' },
  { re: /תביעה ייצוגית|תביעה|הליך משפטי|עיצום|קנס/, w: 6, dir: 'bear' },
  { re: /שינוי החזקות|בעל עניין|עסקה עם בעל שליטה/, w: 5, dir: 'neutral' },
  { re: /מצגת|שיחת ועידה|עדכון פעילות|הודעה לעיתונות/, w: 5, dir: 'neutral' },
]

export function scoreReport(item) {
  const text = item.title || ''
  let score = 4 // ברירת מחדל אם שום כלל לא נתפס
  let direction = 'neutral'
  for (const rule of RULES) {
    if (rule.re.test(text)) {
      score = rule.w
      direction = rule.dir
      break
    }
  }
  if (item.is_priority) score = Math.min(10, score + 1)
  return { materiality_score: score, direction }
}
