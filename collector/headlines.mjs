// סיווג כותרת של ידיעת עיתונות שאין לה חברה מזוהה, לפי מדיניות המשתמש:
//   1. כתבה ענפית  → תגית סקטור (לפי רשימת הבורסה).
//   2. כתבה מאקרו  → רק חשיבות גבוהה (ריבית/תוכנית כלכלית שמשפיעה על סקטור/חברה)
//                    → תגית "מאקרו". משיכה מצומצמת מאוד.
//   3. אחרת        → null (נזרק, לא מוצג).
// דטרמיניסטי (כללי-מילים) — אמין, חינם, ובורר בקפדנות (precision over recall).

// מאקרו — רק טריגרים חזקים של חשיבות גבוהה (ריבית, אינפלציה, מדיניות/תקציב, צמיחה).
const MACRO = /\bריבית\b|בנק ישראל|הריבית|אינפלציה|מדד המחירים לצרכן|הפד\b|פדרל ריזרב|הבנק הפדרלי|תקציב המדינה|הגירעון|דירוג האשראי של ישראל|תחזית צמיחה|התוצר|שיעור האבטלה|מדיניות מוניטרית|תוכנית כלכלית|רפורמה כלכלית/

// סקטורים — טריגרים ברמת ענף (לא חברה ספציפית). הסדר: ספציפי קודם.
const SECTOR = [
  [/שוק הדיור|מחירי הדיור|מחירי הדירות|שוק הנדל"ן|התחלות בנייה|שכר הדירה|ענף הבנייה|ענף הנדל"ן/, 'נדל"ן ובינוי'],
  [/המערכת הבנקאית|מערכת הבנקאות|הבנקים הגדולים|עמלות הבנקים|רווחי הבנקים/, 'בנקים'],
  [/חברות הביטוח|ענף הביטוח|שוק הביטוח/, 'ביטוח'],
  [/בתי ההשקעות|שוק ההון|קרנות הנאמנות|הגופים המוסדיים|החיסכון הפנסיוני/, 'פיננסים'],
  [/ענף ההייטק|חברות ההייטק|תעשיית השבבים|הסקטור הטכנולוגי|בינה מלאכותית|תעשיית הסייבר/, 'טכנולוגיה'],
  [/מחיר הנפט|חביות נפט|אופ"ק|הגז הטבעי|מאגר[יל] הגז|חיפושי הגז/, 'נפט וגז'],
  [/אנרגיה מתחדשת|אנרגיה סולארית|אנרגיה נקייה|משק האנרגיה/, 'אנרגיה מתחדשת'],
  [/חברות התרופות|ענף הפארמה|תעשיית הביומד|חברות הביומד/, 'ביומד'],
  [/ענף הקמעונאות|רשתות השיווק|רשתות המזון|הצריכה הפרטית|יוקר המחיה/, 'מסחר ושירותים'],
  [/התעשייה הביטחונית|תעשיות ביטחוניות|הייצוא הביטחוני/, 'תעשייה'],
]

// מחזיר { tag, type } או null. type ∈ { 'macro', 'sector' }.
export function classifyHeadline(title) {
  const t = title || ''
  for (const [re, sector] of SECTOR) if (re.test(t)) return { tag: sector, type: 'sector' }
  if (MACRO.test(t)) return { tag: 'מאקרו', type: 'macro' }
  return null
}
